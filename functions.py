"""this is a module for the functions for calculator"""

#defining the basic functions
def add(num1, num2):
    """adds to numbers"""

    return num1 + num2


def substract(num1, num2):
    """Substract two numbers"""
    return num1 - num2


def multiply(num1, num2):
    """Multiplies two numbers"""
    return num1 * num2

def devide(num1, num2):
    """devides two numbers"""

    return num1 / num2
